#A for loop -A for loop is a control flow statement for specifying iteration, which allows code to be executed repeatedly.
# A for loop has two parts: a header specifying the iteration, and a body which is executed once per iteration.

#A while loop-The while loop is used to repeat a section of code an unknown number of times until a specific condition is met. 
#For example, say we want to know how many times a given number can be divided by 2 before it is less than or equal to 1.
